package com.udby.blog.mutable.demo;

public class SomeForeignThing {
    private int valueForField;
    private String anotherValue;
    private String completelyDifferentField;

    public int getValueForField() {
        return valueForField;
    }

    public void setValueForField(int valueForField) {
        this.valueForField = valueForField;
    }

    public String getAnotherValue() {
        return anotherValue;
    }

    public void setAnotherValue(String anotherValue) {
        this.anotherValue = anotherValue;
    }

    public String getCompletelyDifferentField() {
        return completelyDifferentField;
    }

    public void setCompletelyDifferentField(String completelyDifferentField) {
        this.completelyDifferentField = completelyDifferentField;
    }
}
